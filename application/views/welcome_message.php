              <h4>About Us</h4>
              Jawahar Navodaya Vidyalayas are Indian schools for talented children and form a part of the system of gifted education. The objectives of the scheme are to provide good quality modern education to the children predominantly from rural areas, without regard to their family's socio-economic condition. First established in 1985, they are the brain child of P.V Narasimha Rao. They were started during Prime Minister ship of Late. Rajiv Gandhi to find and foster talented children from the rural parts of India. They were formerly named as Navodaya Vidyalayas and renamed later as ‘Jawahar Navodaya Vidyalayas' in the birth-centenary year of Pandit Jawahar Lal Nehru, the first Prime Minister of Independent India. Navodaya Vidyalayas are located all over the country, except Tamil Nadu. There are approximately 588 JNVs across India as of now. They offer free education to all students who get selected through the admission process which includes an All India Entrance Exam, held at district level.
